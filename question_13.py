# 13) If each pizza can serve 3 people, write a program to find out how many pizzas are needed for a party of 27 people.

# Number of people at the party
number_of_people = 27

# Number of people served by each pizza
people_per_pizza = 3

# Calculate how many pizzas are needed
pizzas_needed = number_of_people // people_per_pizza

# Print the number of pizzas needed
print(f"The number of pizzas needed for a party of {number_of_people} people is: {pizzas_needed}")
