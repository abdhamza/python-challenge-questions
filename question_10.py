# 10) You have 100 rupees to buy a ticket of movie. Each ticket costs 12 rupees. 
# Write a python program to calculate how many tickets you can buy.

# Total amount you have
total_rupees = 100

# Cost of one movie ticket
ticket_cost = 12

# Calculate how many tickets you can buy
tickets_you_can_buy = total_rupees // ticket_cost

print(f"You can buy {tickets_you_can_buy} movie tickets with 100 rupees.")
