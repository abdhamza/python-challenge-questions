# 11) In the last three cricket games, babar azam scored 22, 28, and 35 . 
# write a python score to Calculate his average score per game.

# Babar Azam's scores in the last three games
scores = [22, 28, 35]

# Calculate the total score
total_score = sum(scores)

# Calculate the number of games
number_of_games = len(scores)

# Calculate the average score per game
average_score = total_score / number_of_games

print(f"Babar Azam's average score per game in the last three games is: {average_score}")
