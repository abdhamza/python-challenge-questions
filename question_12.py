# 12) You're building a rectangular garden that's 20 feet long and 15 feet wide. 
# Calculate the total length of fencing needed to enclose the garden. 
# Do not use if else. only use operators and print function.

# Dimensions of the garden
length = 20
width = 15

# Calculate the total length of fencing needed
total_length_of_fencing = 2 * (length + width)

# Print the total length of fencing needed
print(f"The total length of fencing needed to enclose the garden is: {total_length_of_fencing} feet")
