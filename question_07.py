# 7) Write a program that compares two strings entered by the user (str1 and str2) and prints whether they are equal or not.

# Take input of two strings from the user
str1 = input("Enter the first string: ")
str2 = input("Enter the second string: ")

# Compare the strings and print the result directly in a descriptive way
print("The strings are equal." if str1 == str2 else "The strings are not equal.")
