# 3) Write a program that takes three numbers x,y,z as input and calculate
# x+y+z
# x-y-z
# y-x-z
# z-x-y
# x*y*z
# x/y*z
# x*y/z
# x/y/z
# x%y
# y%z
# z%x
# z**y
# z//y//x
# y//x
# z//x


# Take three numbers as input from the user
x = float(input("Enter the value of x: "))
y = float(input("Enter the value of y: "))
z = float(input("Enter the value of z: "))

# Calculate and print the results
print("x + y + z =", x + y + z)
print("x - y - z =", x - y - z)
print("y - x - z =", y - x - z)
print("z - x - y =", z - x - y)
print("x * y * z =", x * y * z)
print("x / y * z =", x / y * z)
print("x * y / z =", x * y / z)
print("x / y / z =", x / y / z)
print("x % y =", x % y)
print("y % z =", y % z)
print("z % x =", z % x)
print("z ** y =", z ** y)
print("z // y // x =", z // y // x)
print("y // x =", y // x)
print("z // x =", z // x)
